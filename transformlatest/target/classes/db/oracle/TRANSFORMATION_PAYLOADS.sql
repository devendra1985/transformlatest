-- Oracle DDL for transformation payload audit table
--
-- Keyed by CORRELATION_ID (header.correlationId). Stores raw + transformed payload and processing status.
--
-- Suggested statuses/steps:
-- RECEIVED -> SCHEMA_VALIDATED -> VALIDATED -> ENRICHED -> TRANSFORMED
-- FAILED can occur at any step (store error fields too).

CREATE TABLE TRANSFORMATION_PAYLOADS (
  CORRELATION_ID       VARCHAR2(64)     NOT NULL,
  STATUS               VARCHAR2(32)     NOT NULL,
  STEP                 VARCHAR2(32)     NOT NULL,
  RAW_PAYLOAD          CLOB            NULL,
  TRANSFORMED_PAYLOAD  CLOB            NULL,
  RESPONSE_PAYLOAD     CLOB            NULL,
  TOTAL_COUNT          NUMBER(10)       NULL,
  SUCCESS_COUNT        NUMBER(10)       NULL,
  FAILED_COUNT         NUMBER(10)       NULL,
  ERROR_CODE           VARCHAR2(64)     NULL,
  ERROR_MESSAGE        VARCHAR2(4000)   NULL,
  CREATED_AT           TIMESTAMP(6)     DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT           TIMESTAMP(6)     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_TRANSFORMATION_PAYLOADS PRIMARY KEY (CORRELATION_ID)
);

-- Optional index for dashboard queries
CREATE INDEX IDX_TRANSFORM_PAYLOADS_STATUS ON TRANSFORMATION_PAYLOADS (STATUS);
CREATE INDEX IDX_TRANSFORM_PAYLOADS_STEP   ON TRANSFORMATION_PAYLOADS (STEP);

-- Item-level tracking: one row per txInf index for a given correlation id
CREATE TABLE TRANSFORMATION_PAYLOAD_ITEMS (
  CORRELATION_ID       VARCHAR2(64)     NOT NULL,
  TX_INDEX             NUMBER(10)       NOT NULL,
  PAYMENT_ID           VARCHAR2(128)    NULL,
  STATUS               VARCHAR2(32)     NOT NULL,
  STEP                 VARCHAR2(32)     NOT NULL,
  RAW_TXINF            CLOB            NULL,
  TRANSFORMED_PAYLOAD  CLOB            NULL,
  RESPONSE_PAYLOAD     CLOB            NULL,
  ERROR_CODE           VARCHAR2(64)     NULL,
  ERROR_MESSAGE        VARCHAR2(4000)   NULL,
  RETRY_COUNT          NUMBER(10)       DEFAULT 0 NOT NULL,
  CREATED_AT           TIMESTAMP(6)     DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT           TIMESTAMP(6)     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_TRANSFORM_PAYLOAD_ITEMS PRIMARY KEY (CORRELATION_ID, TX_INDEX),
  CONSTRAINT FK_TRANSFORM_PAYLOAD_ITEMS_REQ
    FOREIGN KEY (CORRELATION_ID) REFERENCES TRANSFORMATION_PAYLOADS (CORRELATION_ID)
);

CREATE INDEX IDX_TPI_STATUS ON TRANSFORMATION_PAYLOAD_ITEMS (STATUS);
CREATE INDEX IDX_TPI_PAYMENT_ID ON TRANSFORMATION_PAYLOAD_ITEMS (PAYMENT_ID);

